static int cv5003_set_expo(struct tx_isp_subdev *sd, int value)
{
	int ret = ISP_SUCCESS;
	int it = value & 0xffff;
	int index = (value & 0xffff0000) >> 16;
	int shr0 = 0;
	struct again_lut *lut = cv5003_again_lut;

	/* shr0 = cv5003_attr.integration_time.max - it; */
	shr0 = cv5003_attr.total_height - it;
	shr0 = ((shr0 >> 1) << 1);

	ret += cv5003_write(sd, 0x304a, ((shr0 >> 16) & 0xf));
	ret += cv5003_write(sd, 0x3049, (unsigned char)((shr0 >> 8) & 0xff));
	ret += cv5003_write(sd, 0x3048, (unsigned char)(shr0 & 0xff));

	ret += cv5003_write(sd, 0x3118,   (unsigned char)(lut[index].sensor_again_value)); //set sensor Again
	ret += cv5003_write(sd, 0x311c, (unsigned char)(lut[index].sensor_dgain_value_low)); //set sensor Dgain
	ret += cv5003_write(sd, 0x311d, (unsigned char)(lut[index].sensor_dgain_value_high)); //set sensor Dgain

	return ret;
}

