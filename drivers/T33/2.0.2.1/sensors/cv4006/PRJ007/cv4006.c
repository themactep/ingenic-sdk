static int cv4006_set_fps(struct tx_isp_subdev *sd, int fps)
{
	struct tx_isp_sensor *sensor = sd_to_sensor_device(sd);
	struct tx_isp_sensor_register_info *info = &sensor->info;
	int ret = ISP_SUCCESS;
	int fps_n,vts_n,cur_exp,cur_reg;
	unsigned char reg = 0,is_updata = 0;
	int ORG_MAX_FPS, ORG_MIN_FPS, ORG_FRAME_LENGTH = 0;

	//CV4006，full（remosic) 和 720P（binning，or binning之后ZOOM到其他模式) 的FRAME_LENGTH有较大的差别，这个需要注意。
	//另外，这几个模式，这里实现的时候，默认认为最大帧率都是30fps哈。如果不是，这里需要注意。
	switch (info->default_boot) {
	case 0: //1440P remosaic
		ORG_MAX_FPS = 30;
		ORG_MIN_FPS = 5;
		ORG_FRAME_LENGTH = 0x0C80;
		break;
	case 1: //1440P remosaic
		ORG_MAX_FPS = 30;
		ORG_MIN_FPS = 5;
		ORG_FRAME_LENGTH = 0x0320;
		break;
	default:
		ret = -1;
		ISP_ERROR("Now we do not support this framerate!!!\n");
	}

	fps_n = (fps >> 16) / (fps & 0xffff) * 10;
	fps_n +=  ((fps >> 16) % (fps & 0xffff));

	if(fps_n > (ORG_MAX_FPS * 10) || fps_n < (ORG_MIN_FPS * 10)) {
		ISP_WARNING("warn: fps_n(%d.%d)no in range\n",fps_n / 10, fps_n % 10);
		return -1;
	}

	vts_n = (30 * 10) * ORG_FRAME_LENGTH / fps_n;
	ISP_WARNING("cv4006 set fps_n(%d.%d)! vts_n(0x%04x)\n",fps_n / 10, fps_n % 10,vts_n);

	ret = cv4006_read(sd, 0x304A, &reg);
	cur_reg = reg;
	ret |= cv4006_read(sd, 0x3049, &reg);
	cur_reg <<= 8;
	cur_reg |= reg;
	ret |= cv4006_read(sd, 0x3048, &reg);
	cur_reg <<= 8;
	cur_reg |= reg;

	if(ret) {
		ISP_WARNING("cv4006 set fps_n(%d.%d)! vts_n(0x%04x) fail i2c err(%d)!\n",fps_n / 10, fps_n % 10,vts_n,ret);
		return -1;
	}

	if(cur_reg >= (vts_n - 4)){
		cur_exp = 4;
		is_updata =1;
	}

	ret = cv4006_write(sd, 0x301C, (unsigned char)(vts_n & 0xff));
	ret |= cv4006_write(sd, 0x301D, (unsigned char)((vts_n >> 8) & 0xff));
	ret |= cv4006_write(sd, 0x301E, (unsigned char)((vts_n >> 16) & 0xff));
	if(ret) {
		ISP_WARNING("cv4006 set fps_n(%d.%d)! vts_n(0x%04x) fail set vts err(%d)!\n",fps_n / 10, fps_n % 10,vts_n,ret);
		return -1;
	}

	sensor->video.fps.value = fps;

#ifndef SENSOR_WDR_2_FRAME
	/* Linear mode */
	switch (info->default_boot) {
	case 0:
		sensor->video.attr->total_height = vts_n;
		sensor->video.attr->integration_time.max = vts_n - 8;
		break;
	case 1:
		sensor->video.attr->total_height = vts_n;
		sensor->video.attr->integration_time.max = vts_n - 1;
		break;
	default:
		ret = -1;
		ISP_ERROR("Now we do not support this framerate!!!\n");
	}
#else
	/* WDR mode */
	switch (info->default_boot) {
	case 0:
		sensor->video.attr->total_height = vts_n;
		sensor->video.attr->integration_time.max = vts_n - 8;
		break;
	case 1:
		sensor->video.attr->total_height = vts_n;
		sensor->video.attr->integration_time.max = vts_n - xx;
		sensor->video.attr->integration_time_short.max = vts_n - xx;
		break;
	default:
		ret = -1;
		ISP_ERROR("Now we do not support this framerate!!!\n");
	}
#endif /* SENSOR_WDR_2_FRAME */

	ret = tx_isp_call_subdev_notify(sd, TX_ISP_EVENT_SYNC_SENSOR_ATTR, &sensor->video);
	if (ret) {
		ISP_WARNING("Description Failed to synchronize the attributes of sensor!!!");
	}

	if(is_updata) {
		ret = cv4006_set_expo(sd,cur_exp);
		if(ret < 0) {
			ISP_WARNING("cv4006 set fps_n(%d.%d)! vts_n(0x%04x) fail set exp err(%d)!\n",fps_n / 10, fps_n % 10,vts_n,ret);
			return -1;
		}
	}

	return ret;
}

static int cv4006_set_hvflip(struct tx_isp_subdev *sd, void *arg)
{
#ifdef SENSOR_MIR_FLIP
	struct tx_isp_hvflip_par *par = (struct tx_isp_hvflip_par *)arg;
	struct tx_isp_sensor *sensor = tx_isp_get_subdev_hostdata(sd);
	int ret = ISP_SUCCESS;
	unsigned char val = 0;

	par->drop_frame = 0;
	par->reset = 0;

	/* 2'b01:mirror,2'b10:filp */
	ret = cv4006_read(sd, 0x3028, &val);
	switch(par->hvflip) {
	case TX_ISP_SENSOR_HVFLIP_NOMAL:
		val &= 0xFC;
		sensor->video.mbus.code = TISP_VI_FMT_SRGGB10_1X10;
		break;
	case TX_ISP_SENSOR_HVFLIP_HFLIP:
		val = ((val & 0xFD) | 0x01);
		sensor->video.mbus.code = TISP_VI_FMT_SGRBG10_1X10;
		break;
	case TX_ISP_SENSOR_HVFLIP_VFLIP:
		val = ((val & 0xFE) | 0x02);
		sensor->video.mbus.code = TISP_VI_FMT_SGBRG10_1X10;
		break;
	case TX_ISP_SENSOR_HVFLIP_HVFLIP:
		val |= 0x03;
		sensor->video.mbus.code = TISP_VI_FMT_SBGGR10_1X10;
		break;
	}
	ret += cv4006_write(sd, 0x3028, val);

	sensor->video.hvflip_mode = par->hvflip;
	sensor->video.mbus_change = 1;
	cv4006_attr_set(sd, wsize);


	return ret;
#else
	return -1;
#endif /* SENSOR_MIR_FLIP */
}

